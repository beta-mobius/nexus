package com.mobius.nexus.tools;

import java.io.*;
import java.nio.file.*;
import java.util.jar.*;
import java.util.zip.ZipEntry;

/**
 * жЁЎеқ—жү“еҢ…е·Ҙе…·
 * е°ҶжЁЎеқ—зӣ®еҪ•жү“еҢ…дёәJARж–Үд»¶пјҢиҮӘеҠЁз”ҹжҲҗmodule.properties
 */
public class ModulePackager {
    
    /**
     * жү“еҢ…жЁЎеқ—
     * 
     * @param sourceDir жЁЎеқ—жәҗд»Јз Ғзӣ®еҪ?     * @param outputJar иҫ“еҮәJARж–Үд»¶и·Ҝеҫ„
     * @param moduleName жЁЎеқ—еҗҚз§°
     * @param version жЁЎеқ—зүҲжң¬
     * @param dependencies дҫқиө–еЈ°жҳҺпјҲйҖ—еҸ·еҲҶйҡ”пј?     * @throws IOException еҰӮжһңжү“еҢ…еӨұиҙҘ
     */
    public static void packageModule(Path sourceDir, Path outputJar, 
                                    String moduleName, String version, 
                                    String dependencies) throws IOException {
        
        if (!Files.exists(sourceDir)) {
            throw new IOException("Source directory not found: " + sourceDir);
        }
        
        // еҲӣе»әиҫ“еҮәзӣ®еҪ•
        Files.createDirectories(outputJar.getParent());
        
        // еҲӣе»әJARж–Үд»¶
        try (JarOutputStream jos = new JarOutputStream(
                new FileOutputStream(outputJar.toFile()))) {
            
            // ж·»еҠ META-INF/module.properties
            addModuleProperties(jos, moduleName, version, dependencies);
            
            // ж·»еҠ зұ»ж–Үд»?            addClassFiles(jos, sourceDir, sourceDir);
            
            // ж·»еҠ иө„жәҗж–Үд»¶
            addResourceFiles(jos, sourceDir, sourceDir);
        }
        
        System.out.println("Module packaged: " + outputJar);
        System.out.println("  Module: " + moduleName + " v" + version);
        if (dependencies != null && !dependencies.trim().isEmpty()) {
            System.out.println("  Dependencies: " + dependencies);
        }
    }
    
    /**
     * ж·»еҠ жЁЎеқ—еұһжҖ§ж–Үд»?     */
    private static void addModuleProperties(JarOutputStream jos, 
                                           String moduleName, String version,
                                           String dependencies) throws IOException {
        JarEntry entry = new JarEntry("META-INF/module.properties");
        jos.putNextEntry(entry);
        
        Properties props = new Properties();
        props.setProperty("module.name", moduleName);
        props.setProperty("module.version", version);
        
        if (dependencies != null && !dependencies.trim().isEmpty()) {
            props.setProperty("module.dependencies", dependencies);
        }
        
        props.setProperty("module.description", "Generated by ModulePackager");
        
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        props.store(baos, "Module properties");
        
        jos.write(baos.toByteArray());
        jos.closeEntry();
    }
    
    /**
     * ж·»еҠ зұ»ж–Үд»?     */
    private static void addClassFiles(JarOutputStream jos, Path rootDir, Path currentDir) 
            throws IOException {
        
        try (DirectoryStream<Path> stream = Files.newDirectoryStream(currentDir)) {
            for (Path file : stream) {
                if (Files.isDirectory(file)) {
                    addClassFiles(jos, rootDir, file);
                } else if (file.toString().endsWith(".class")) {
                    addFileToJar(jos, rootDir, file);
                }
            }
        }
    }
    
    /**
     * ж·»еҠ иө„жәҗж–Үд»¶
     */
    private static void addResourceFiles(JarOutputStream jos, Path rootDir, Path currentDir) 
            throws IOException {
        
        try (DirectoryStream<Path> stream = Files.newDirectoryStream(currentDir)) {
            for (Path file : stream) {
                if (Files.isDirectory(file)) {
                    // и·іиҝҮMETA-INFзӣ®еҪ•пјҲе·Із»ҸеӨ„зҗҶпјү
                    if (!file.getFileName().toString().equals("META-INF")) {
                        addResourceFiles(jos, rootDir, file);
                    }
                } else if (!file.toString().endsWith(".class")) {
                    // йқһзұ»ж–Үд»¶дҪңдёәиө„жәҗж·»еҠ 
                    addFileToJar(jos, rootDir, file);
                }
            }
        }
    }
    
    /**
     * ж·»еҠ ж–Үд»¶еҲ°JAR
     */
    private static void addFileToJar(JarOutputStream jos, Path rootDir, Path file) 
            throws IOException {
        
        // и®Ўз®—JARеҶ…зҡ„зӣёеҜ№и·Ҝеҫ„
        String relativePath = rootDir.relativize(file).toString()
                .replace(File.separatorChar, '/');
        
        JarEntry entry = new JarEntry(relativePath);
        jos.putNextEntry(entry);
        
        // еҶҷе…Ҙж–Үд»¶еҶ…е®№
        Files.copy(file, jos);
        jos.closeEntry();
    }
    
    /**
     * е‘Ҫд»ӨиЎҢжҺҘеҸ?     */
    public static void main(String[] args) {
        if (args.length < 3) {
            System.err.println("Usage: ModulePackager <source-dir> <output-jar> <module-name> [version] [dependencies]");
            System.err.println("Example: ModulePackager ./target/classes ./my-module.jar com.example.module 1.0.0 \"core:[1.0,2.0),util:1.5.0\"");
            System.exit(1);
        }
        
        try {
            Path sourceDir = Paths.get(args[0]);
            Path outputJar = Paths.get(args[1]);
            String moduleName = args[2];
            String version = args.length > 3 ? args[3] : "1.0.0";
            String dependencies = args.length > 4 ? args[4] : "";
            
            packageModule(sourceDir, outputJar, moduleName, version, dependencies);
            
        } catch (Exception e) {
            System.err.println("Packaging failed: " + e.getMessage());
            e.printStackTrace();
            System.exit(1);
        }
    }
}